    private void addFacultyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFacultyButtonActionPerformed
        // TODO add your handling code here:
       try{
           
           if(firstName.getText() !=null && !firstName.getText().trim().equals("") && titleTextField.getText()!= null && !titleTextField.getText().trim().equals("")){    
                       
               FacultyBean facultyBean = new FacultyBean();
               facultyBean.setFirstName(firstName.getText());
                facultyBean.setLastName(lastName.getText()); 
                facultyBean.setGradSchool(gradSchool.getText());
                facultyBean.setDaysToTeach(daysToTeach.getText());
                facultyBean.setTitle(titleTextField.getText());
                facultyBean.setDegree(degree.getText());
                facultyBean.setMaxLoadFall(Double.valueOf(maxLoadFall.getText())); 
                facultyBean.setMaxLoadSpring(Double.valueOf(maxLoadSpring.getText()));
                facultyBean.setMaxLoadSummer(Double.valueOf(maxLoadSummer.getText()));                
                new FacultyModel().addFaculty(facultyBean);
           
           facultyMsgLabel.setText("Registration is Successfull.");           
           loadDataIntoJTable();                      
            firstName.setText("");
            lastName.setText("");
            gradSchool.setText("");
            degree.setText("");           
            titleTextField.setText("");
            daysToTeach.setText("");
            maxLoadFall.setText("");
            maxLoadSpring.setText("");
            maxLoadSummer.setText("");
           }else{
               facultyMsgLabel.setText("FirstName and Title Should not be Empty.");
           }
       }catch(Exception exp){
           JOptionPane.showMessageDialog(null, exp);
       } 
        
    }//GEN-LAST:event_addFacultyButtonActionPerformed

