    private void updateFacultyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateFacultyButtonActionPerformed
        // TODO add your handling code here:
         try{
           if(new FacultyModel().isFacultyExist(firstName.getText())){                     
               FacultyBean facultyBean = new FacultyBean();
               facultyBean.setFirstName(firstName.getText());
                facultyBean.setLastName(lastName.getText()); 
                facultyBean.setGradSchool(gradSchool.getText());
                facultyBean.setDaysToTeach(daysToTeach.getText());
                facultyBean.setTitle(titleTextField.getText());
                facultyBean.setDegree(degree.getText());
                facultyBean.setMaxLoadFall(Double.valueOf(maxLoadFall.getText())); 
                facultyBean.setMaxLoadSpring(Double.valueOf(maxLoadSpring.getText()));
                facultyBean.setMaxLoadSummer(Double.valueOf(maxLoadSummer.getText()));  
                
                new FacultyModel().updateFaculty(facultyBean);
          loadDataIntoJTable();          
           
           facultyMsgLabel.setText("Updation is Successfull.");
         }else{
                 facultyMsgLabel.setText("Please enter valid Faculty FirstName ");
              }
       }catch(Exception exp){
           JOptionPane.showMessageDialog(null, exp);
       } 
    }//GEN-LAST:event_updateFacultyButtonActionPerformed

