    private void updateSemesterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateSemesterButtonActionPerformed
        // TODO add your handling code here:
        try{
           
           if(semester.getText() !=null && !semester.getText().trim().equals("") ){    
                      
               SemesterModel semesterModel = new SemesterModel();
                if(semesterModel.isSemesterExist(semester.getText())){
               SemesterBean semesterBean = new SemesterBean();
               semesterBean.setSemester(semester.getText());
               semesterBean.setStartDate(startDate.getText());
               semesterBean.setEndDate(endDate.getText());
               semesterModel.updateSemester(semesterBean);
           semesterMsgLabel.setText("Updation is Successfull.");           
           loadDataIntoJTable();                      
                        
           }else{
               semesterMsgLabel.setText("Semester does not exist.");
           }
           }else{
               semesterMsgLabel.setText("Semester Should not be Empty.");
           }
       }catch(Exception exp){
           JOptionPane.showMessageDialog(null, exp);
       } 
        
    }//GEN-LAST:event_updateSemesterButtonActionPerformed

    private void deleteSemesterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteSemesterButtonActionPerformed
        // TODO add your handling code here:
        try{
           
           if(semester.getText() !=null && !semester.getText().trim().equals("") ){    
                       
           SemesterModel semesterModel = new SemesterModel();
         boolean flag =  semesterModel.deleteSemester(semester.getText());
         if(flag){
           semesterMsgLabel.setText("Deletion is Successfull.");        
         }else{
             semesterMsgLabel.setText("Semester does not exist.");
         }
           loadDataIntoJTable();                      
            semester.setText("");
            startDate.setText("");
            endDate.setText("");
            
           }else{
               semesterMsgLabel.setText("Semester Should not be Empty.");
           }
       }catch(Exception exp){
           JOptionPane.showMessageDialog(null, exp);
       } 
        
    }//GEN-LAST:event_deleteSemesterButtonActionPerformed

