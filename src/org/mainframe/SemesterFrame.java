    private void addSemesterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addSemesterButtonActionPerformed
        // TODO add your handling code here:
       try{
           
           if(semester.getText() !=null && !semester.getText().trim().equals("") ){    
                       
               SemesterModel semesterModel = new SemesterModel();
               SemesterBean semesterBean = new SemesterBean();
               semesterBean.setSemester(semester.getText());
               semesterBean.setStartDate(startDate.getText());
               semesterBean.setEndDate(endDate.getText());
               semesterModel.addSemester(semesterBean);
           semesterMsgLabel.setText("Registration is Successfull.");           
           loadDataIntoJTable();                      
            semester.setText("");
            startDate.setText("");
            endDate.setText("");
            
           }else{
               semesterMsgLabel.setText("Semester Should not be Empty.");
           }
       }catch(Exception exp){
           JOptionPane.showMessageDialog(null, exp);
       } 
        
    }//GEN-LAST:event_addSemesterButtonActionPerformed

